#include "io.h"

void	m_putchar(char c)
{
	write(STDOUT, &c, 1);
}

void	m_putstr(char *c)
{
	write(STDOUT, c, m_strlen(c));
}

void	m_putnbr_base(int n, int base)
{
	m_putstr(m_itoa_base(n, base));
}
