#include "io.h"

void	m_putchar(char c)
{
	write(STDOUT, &c, 1);
}

void	m_putstr(char *c)
{
	write(STDOUT, c, m_strlen(c));
}

void	m_putnbr(int n)
{
	if (n >= 10)
		m_putnbr(n / 10);
	m_putchar(0x30 + (n % 10));
}
