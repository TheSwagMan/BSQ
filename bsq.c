#include "bsq.h"

#include "reading.h"

int		main(int ac, char **av)
{
	int	i;
	int	fd;

	i = 1;
	if (ac > 1)
	{
		while (i < ac)
		{
			fd = open(av[i++], O_RDONLY);
			if (fd <= 0)
				return (-1);
			get_input_as_bit(fd);
			close(fd);
		}
	}
	else
	{
		
	}
	return (0);
}
